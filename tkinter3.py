import tkinter as tk
root=tk.Tk()
root.geometry("600x400")
root.title("demo")
row_1=tk.Frame(root,bg="red")
button_1=tk.Button(row_1,text="1",fg="blue")
button_1.grid(row=0,column=0)
button_2=tk.Button(row_1,text="2",fg="blue")
button_2.grid(row=0,column=1)
button_3=tk.Button(row_1,text="3",fg="blue")
button_3.grid(row=0,column=2)
button_4=tk.Button(row_1,text="4",fg="blue")
button_4.grid(row=1,column=0)
button_5=tk.Button(row_1,text="5",fg="blue")
button_5.grid(row=1,columnspan=2)
button_6=tk.Button(row_1,text="6",fg="blue")
button_6.grid(row=1,column=2,rowspan=2)
row_1.pack(fill='y',expand=1)
root.mainloop()